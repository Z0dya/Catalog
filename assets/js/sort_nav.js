function myFunction2() {
    document.getElementById("myDropdown-nav2").classList.toggle("show-nav");
    document.getElementById("myDropdown-nav3").classList.remove("show-nav");
    document.getElementById("myDropdown-nav4").classList.remove("show-nav");
    document.getElementById("myDropdown-nav5").classList.remove("show-nav");
    document.getElementById("myDropdown-nav6").classList.remove("show-nav");
}

// Close the dropdown if the user clicks outside of it
window.onclick = function (event) {
    if (!event.target.matches('.dropbtn-nav')) {

        let dropdowns = document.getElementsByClassName("dropdown-content-nav");
        let i;
        for (i = 0; i < dropdowns.length; i++) {
            let openDropdown = dropdowns[i];
            if (openDropdown.classList.contains('show-nav')) {
                openDropdown.classList.remove('show-nav');
            }
        }
    }
}

